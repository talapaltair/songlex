function setupTabs() {
  document.querySelectorAll("[data-tabs]").forEach((tabs) => {
    tabs.addEventListener("click", (event) => {
      const button = event.target.closest("[data-tab]");
      if (!button) return;
      const tabName = button.dataset.tab;
      tabs.querySelectorAll("[data-tab]").forEach((item) => {
        const active = item === button;
        item.classList.toggle("active", active);
        item.setAttribute("aria-selected", String(active));
      });
      tabs.querySelectorAll(".tab-panel").forEach((panel) => {
        panel.classList.toggle("active", panel.id === tabName);
      });
    });
  });
}

function setupNavigation() {
  navToggle.addEventListener("click", () => {
    const isOpen = mainNav.classList.toggle("open");
    document.body.classList.toggle("nav-open", isOpen);
    navToggle.setAttribute("aria-expanded", String(isOpen));
  });

  mainNav.addEventListener("click", (event) => {
    if (event.target.matches("a")) {
      mainNav.classList.remove("open");
      document.body.classList.remove("nav-open");
      navToggle.setAttribute("aria-expanded", "false");
    }
  });
}

function setupTaskButtons() {
  document.addEventListener("click", (event) => {
    const startButton = event.target.closest(".start-task");
    if (!startButton) return;
    const module = document.querySelector(startButton.getAttribute("href"));
    const firstDetails = module?.querySelector("details");
    if (firstDetails) firstDetails.open = true;
  });
}

function setupContactForm() {
  contactForm.addEventListener("submit", (event) => {
    event.preventDefault();
    formNote.textContent = "This is a placeholder form. In Tilda, connect it to email or Google Sheets.";
  });
}

