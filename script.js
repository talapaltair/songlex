function icon(id) {
  return `<svg aria-hidden="true"><use href="#${id}"></use></svg>`;
}

function list(items) {
  return `<ul>${items.map((item) => `<li>${item}</li>`).join("")}</ul>`;
}

function orderedList(items) {
  return `<ol>${items.map((item) => `<li>${item}</li>`).join("")}</ol>`;
}

function targetList(items) {
  return `<ul class="target-list">${items.map((item) => `<li>${item}</li>`).join("")}</ul>`;
}

function matchTable(items) {
  return `
    <table class="mini-table">
      <thead><tr><th>Word or phrase</th><th>Meaning</th></tr></thead>
      <tbody>
        ${items.map(([word, meaning]) => `<tr><td>${word}</td><td>${meaning}</td></tr>`).join("")}
      </tbody>
    </table>
  `;
}

function details(title, body) {
  return `
    <details>
      <summary>${title}</summary>
      <div class="details-body">${body}</div>
    </details>
  `;
}

function renderProgress() {
  const saved = JSON.parse(localStorage.getItem("songlex-progress") || "{}");
  progressTracker.innerHTML = units.map((unit) => `
    <label class="progress-item">
      <input type="checkbox" data-progress="${unit.id}" ${saved[unit.id] ? "checked" : ""}>
      Unit ${unit.id}
    </label>
  `).join("");

  progressTracker.addEventListener("change", (event) => {
    const input = event.target.closest("[data-progress]");
    if (!input) return;
    const state = JSON.parse(localStorage.getItem("songlex-progress") || "{}");
    state[input.dataset.progress] = input.checked;
    localStorage.setItem("songlex-progress", JSON.stringify(state));
  });
}

